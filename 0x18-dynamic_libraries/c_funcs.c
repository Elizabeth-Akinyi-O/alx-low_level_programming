#include <stdio.h>

/**
 * add - adds 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum
 */

int add(int a, int b)
{
	/* int sum; */

	/* sum = a + b; */
	/*return (sum);*/
	return (a + b);
}

/**
 * sub - subtracts 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: division
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the modulus of 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: modulus
 */

int mod(int a, int b)
{
	return (a % b);
}
